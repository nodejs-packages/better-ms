/**
 * Helpers.
 */

const now = new Date();
const ms = 1;
const s = ms * 1000;
const m = s * 60;
const h = m * 60;
const d = h * 24;
const w = d * 7;
const month = new Date(now.getFullYear(), now.getMonth() + 1, 0).getDate();
const mth = d * month;
const y = d * 365.25;

/**
 * Parse or format the given `val`.
 *
 * Options:
 *
 *  - `long` verbose formatting [false]
 *
 * @param {String|Number} val
 * @param {Object} [options]
 * @throws {Error} throw an error if val is not a non-empty string or a number
 * @return {String|Number}
 * @api public
 */

module.exports = function(val, options) {
	options = options || {};
	var type = typeof val;
	if (type === 'string' && val.length > 0) {
		return parse(val);
	} else if (type === 'number' && isFinite(val)) {
		return options.long ? fmtLong(val) : fmtShort(val);
	}
	throw new Error('val is not a non-empty string or a valid number. val=' + JSON.stringify(val));
};

/**
 * Parse the given `str` and return milliseconds.
 *
 * @param {String} str
 * @return {Number}
 * @api private
 */

function parse(str) {
	str = String(str);
	if (str.length > 100) {
		return;
	}
	const match = /^(-?(?:\d+)?\.?\d+) *(milliseconds?|msecs?|ms|seconds?|secs?|s|minutes?|mins?|m|hours?|hrs?|h|days?|d|weeks?|w|years?|yrs?|y)?$/i.exec(str);
	if (!match) {
		return;
	}

	const n = parseFloat(match[1]);
	const type = (match[2] || 'ms').toLowerCase();

	const availableTypes = [
		{ types: ['years', 'year', 'yrs', 'yr', 'y'], logic: n * y },
		{ types: ['weeks', 'week', 'w'], logic: n * w },
		{ types: ['days', 'day', 'd'], logic: n * d },
		{ types: ['hours', 'hour', 'hrs', 'hr', 'h'], logic: n * h },
		{ types: ['minutes', 'minute', 'mins', 'min', 'm'], logic: n * m },
		{ types: ['seconds', 'second', 'secs', 'sec', 's'], logic: n * s },
		{ types: ['milliseconds', 'millisecond', 'msecs', 'msec', 'ms'], logic: n }
	];

	return availableTypes.find(element => element.types.includes(type) || undefined).logic;
}

/**
 * Short format for `ms`.
 *
 * @param {Number} ms
 * @return {String}
 * @api private
 */

function fmtShort(ms) {
	const msAbs = Math.abs(ms);
	if (msAbs >= d) {
		return Math.round(ms / d) + 'd';
	}
	if (msAbs >= h) {
		return Math.round(ms / h) + 'h';
	}
	if (msAbs >= m) {
		return Math.round(ms / m) + 'm';
	}
	if (msAbs >= s) {
		return Math.round(ms / s) + 's';
	}
	return ms + 'ms';
}

/**
 * Long format for `ms`.
 *
 * @param {Number} ms
 * @return {String}
 * @api private
 */

function fmtLong(ms) {
	const msAbs = Math.abs(ms);
	if (msAbs >= d) {
		return plural(ms, msAbs, d, 'day');
	}
	if (msAbs >= h) {
		return plural(ms, msAbs, h, 'hour');
	}
	if (msAbs >= m) {
		return plural(ms, msAbs, m, 'minute');
	}
	if (msAbs >= s) {
		return plural(ms, msAbs, s, 'second');
	}
	return ms + ' ms';
}

/**
 * Pluralization helper.
 */

function plural(ms, msAbs, n, name) {
	const isPlural = msAbs >= n * 1.5;
	return Math.round(ms / n) + ' ' + name + (isPlural ? 's' : '');
}
